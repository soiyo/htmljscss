// 종류 : &&, ||, !
// x && y : x가 참이면 y 반환, 거짓이면 x 반환
// x || y : x가 참이면 x 반환, 거짓이면 y 반환
// !x : x가 참이면 fause 반환, 거짓이면 true 반환

