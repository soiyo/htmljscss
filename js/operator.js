// 3. 비교 연산자
// 피연산자를 비교한 뒤 논리형 값인 참, 거짓을 반환
// x == y : x와 y의 값이 같으면 true 반환
// x === y : x와 y의 값이 같고, 자료형도 같으면 true 반환
console.log(10 == "10"); //true
console.log(10 === "10"); //false

// 4. 논리 연산자
